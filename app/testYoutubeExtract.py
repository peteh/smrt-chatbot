import youtube_transcript_api
import youtube_transcript_api.formatters
import urllib.parse

def video_id(value):
    """
    Examples:
    - http://youtu.be/SA2iWivDJiE
    - http://www.youtube.com/watch?v=_oPAwA_Udwc&feature=feedu
    - http://www.youtube.com/embed/SA2iWivDJiE
    - http://www.youtube.com/v/SA2iWivDJiE?version=3&amp;hl=en_US
    """
    query = urllib.parse.urlparse(value)
    if query.hostname == 'youtu.be':
        return query.path[1:]
    if query.hostname in ('www.youtube.com', 'youtube.com'):
        if query.path == '/watch':
            p = urllib.parse.parse_qs(query.query)
            return p['v'][0]
        if query.path[:7] == '/embed/':
            return query.path.split('/')[2]
        if query.path[:3] == '/v/':
            return query.path.split('/')[2]
    # fail?
    return None

videoId = video_id('https://www.youtube.com/watch?v=_ZP0WldI3-U')
transcript = youtube_transcript_api.YouTubeTranscriptApi.get_transcript(videoId)
textFormatter = youtube_transcript_api.formatters.TextFormatter()
text = textFormatter.format_transcript(transcript)
print(text)